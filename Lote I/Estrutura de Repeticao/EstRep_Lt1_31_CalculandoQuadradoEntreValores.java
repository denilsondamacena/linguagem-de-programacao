public class EstRep_Lt1_31_CalculandoQuadradoEntreValores
{
    public static void main(String args[])
    {
        
        for (int cont = 10; cont <= 150; cont++)
        {
            System.out.println("O quadrado de "+ cont + " é = " + (cont * cont));
        }
    }
}